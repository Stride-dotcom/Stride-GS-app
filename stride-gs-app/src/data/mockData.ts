// ─── Dashboard Stats ──────────────────────────────────────────────────────────

export function getDashboardStats() {
  const activeItems = inventoryItems.filter((i) => i.status === 'Active').length;
  const onHoldItems = inventoryItems.filter((i) => i.status === 'On Hold').length;
  const openTasks = tasks.filter((t) => t.status === 'Open').length;
  const pendingWillCalls = willCalls.filter(
    (wc) => wc.status === 'Pending' || wc.status === 'Scheduled'
  ).length;

  return { activeItems, onHoldItems, openTasks, pendingWillCalls };
}
